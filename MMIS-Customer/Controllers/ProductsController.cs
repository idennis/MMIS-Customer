        // GET: /Products/
        public ActionResult Index(string productName, string category)
        {
            var CategoryList = new List<string>();

            var CategoryQry = from g in db.Categories
                              orderby g.Title
                              select g.Title;

            CategoryList.AddRange(CategoryQry.Distinct());

            ViewBag.category = new SelectList(CategoryList);


            /* selects products from database and save as var */
            var products = from p in db.Products
                           select p;

            if (!String.IsNullOrEmpty(productName))
            {
                products = products.Where(p => p.Title.Contains(productName));
            }

            if (!String.IsNullOrEmpty(category))
            {
                products = products.Where(p => p.Categories.Any(x => x.Title == category));
                
            }
            
            return View(products);
        }

        // GET: /Products/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Product product = db.Products.Find(id);
            if (product == null)
            {
                return HttpNotFound();
            }
            return View(product);
        }

